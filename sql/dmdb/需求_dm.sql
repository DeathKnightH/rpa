COMMENT ON COLUMN TXBBG03.REC_REVISOR_NAME IS '记录修改人姓名';
COMMENT ON COLUMN TXBBG03.REC_REVISOR_TIME IS '记录修改时间';
COMMENT ON COLUMN TXBBG03.ARCHIVE_FLAG IS '归档标记';
COMMENT ON COLUMN TXBBG03.DEL_FLAG IS '记录删除标记';
COMMENT ON COLUMN TXBBG03.TENANT_USER IS '租户';
COMMENT ON COLUMN TXBBG03.UNIT_CODE IS '业务单元代码';
COMMENT ON COLUMN TXBBG03.SEG_NO IS '系统账套';
