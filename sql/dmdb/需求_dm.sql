-- 需求数据
CREATE TABLE TXBBG03 (
	UUID VARCHAR2(32) DEFAULT ' ' NOT NULL,
	REC_CREATOR VARCHAR2(32) DEFAULT ' ' NOT NULL,
	REC_CREATOR_NAME VARCHAR2(100) DEFAULT ' ' NOT NULL,
    REC_CREATE_TIME VARCHAR2(17) DEFAULT ' ' NOT NULL,
	REC_REVISOR VARCHAR2(32) DEFAULT ' ' NOT NULL,
	REC_REVISOR_NAME VARCHAR2(100) DEFAULT ' ' NOT NULL,
	REC_REVISOR_TIME VARCHAR2(17) DEFAULT ' ' NOT NULL,
    ARCHIVE_FLAG VARCHAR2(1) DEFAULT ' ' NOT NULL,
	DEL_FLAG NUMBER(1,0) DEFAULT 0 NOT NULL,
    TENANT_USER VARCHAR(10) DEFAULT ' ' NOT NULL,
	UNIT_CODE VARCHAR(10) DEFAULT ' ' NOT NULL,
	SEG_NO VARCHAR2(10) DEFAULT ' ' NOT NULL,
	RECORD_DATE date NOT NULL,
  	INDICATOR_NAME varchar(128) NOT NULL,
	INDICATOR_ID varchar(32) NOT NULL,
  	INDICATOR_UNIT varchar(32) DEFAULT ' ' NOT NULL,
  	INDICATOR_FREQUENCY varchar(1) NOT NULL,
  	INDICATOR_VALUE varchar(64) NOT NULL,
	INDICATOR_RESOURCE varchar(64) DEFAULT ' ' NOT NULL,
  	INDICATOR_TITLE varchar(255) NOT NULL,
  	DATA_SOURCE varchar(64) NOT NULL,
	CONSTRAINT TXBBG03_PK PRIMARY KEY (INDICATOR_ID, RECORD_DATE, DATA_SOURCE)
);
